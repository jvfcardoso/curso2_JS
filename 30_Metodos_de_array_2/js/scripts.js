// splice (adicionar um elemento entre outros elementos)
var arr = [1,2,3,4,5];

arr.splice(3, 0, 999);

console.log(arr);

// splice (remover um elemento específico no meio do array)
arr.splice(4, 1);

console.log(arr);

// indexOf (achar o índice de um elemento específico)
console.log(arr.indexOf(5));

// join (transformar um array em string)
var arr2 = ["O", "rato", "roeu", "a", "roupa"];

console.log(arr2.join(" "));

// reverse (reverter a posição de um array)

console.log(arr2.reverse());

